
# O DESAFIO DO DIA 01

# O desafio a ser feito neste projeto é criar um programa em Python
# que seja capaz de gerar nomes de bandas. 

# O programa deve solicitar ao usuário a cidade onde ele cresceu e o nome do seu animal de estimação.

# Em seguida, deve combinar essas informações para formar o nome da banda. 

# O programa deve imprimir uma saudação inicial, solicitar a cidade onde o usuário cresceu, 
# armazenar essa informação em uma variável, solicitar o nome do animal de estimação, armazenar 
# essa informação em outra variável, combinar as duas informações e imprimir o nome da banda resultante.

# Além disso, o programa deve garantir que o cursor de entrada apareça em uma nova linha após cada 
# pergunta feita ao usuário.


# SOLUÇÃO

# 1. Imprimir uma saudação inicial
print("Bem vindo ao gerador de nomes de bandas!")
# 2. Solicitar a cidade onde o usuário cresceu
cidade = input("Qual cidade você cresceu?\n")
# 3. Solicitar o nome do animal de estimação
animal = input("Qual o nome do seu animal de estimação?\n")
# 4. Combinar as duas informações e imprimir o nome da banda resultante
print("O nome da sua banda é: " + cidade + " " + animal)
# 5. Garantir que o cursor de entrada apareça em uma nova linha após cada pergunta feita ao usuário.
print("\n")


